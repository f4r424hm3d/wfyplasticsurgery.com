            <h4 class="mb-0">Why Choose India?</h4>
            <p>India is the seventh-largest country, by area, in the world. It is the land of historic trade routes,
              vast empires, exotic spices, and precious jewels. India’s history dates back several thousand years from
              ancient times to the more modern. Over the years, India has grown and developed and now has one of the
              fastest-growing major economies in the world. India has risen to become a top healthcare destination.
              India has the capability to host a variety of abroad patients and both provide them with excellent
              medical care, as well treat their soul with unforgettable experiences during their visit.</p>

            <h5 class="mb-0">About healthcare facilities in India</h5>
            <p>India is on par with global healthcare standards and houses nearly 21 best hospitals that are
              accredited by the Joint Commission International – the commission that works to improve patient safety
              and quality of healthcare in the international community by offering education, publications, advisory
              services via international accreditation and certification. These facilities ensure that the utmost care
              is taken to line up with the standards of competitors in the West. Hospitals in India offer the latest
              and best in medical technologies, equivalent to their western counterparts. The treatments are
              additionally complemented with compassionate and attentive aftercare. Healthcare costs in India are a
              fraction of the costs seen in western countries but the quality remains uncompromised.</p>

            <h5 class="mb-0">Well trained and highly skilled professionals</h5>
            <p>Many doctors in India have trained at top universities around the world, and have returned to India to
              dedicate their knowledge, training, and skills to those patients, that are unable to get the same level
              of care elsewhere, due to high costs. Factoring in India’s own excellent educational system, doctors
              that studied in India are equally qualified as their colleagues who studied abroad. The doctors at
              Indian hospitals are trained to use the best and latest technologies in healthcare to treat patients.
              They are current in new trends and procedures in the field of medicine with continuing training and
              research keeping them competitive with their counterparts in the west. Their skill and knowledge have
              been the backbone of the healthcare industry in India.</p>

            <h5 class="mb-0">Availability of alternative therapies and medicine</h5>
            <p>Medicine got an early start in India; about 1000 BC, doctors in India wrote the Atharva Veda, a medical
              textbook that explained how to treat diseases. Around 500 BC, the Samhita was written. This outlined
              surgical procedures. To summarize, India has been a forerunner in the medical field from times long
              past. India introduced the world to alternative treatments like Ayurveda, Naturopathy, Homeopathy, Yoga,
              Acupuncture, Acupressure, Hydropathy, Reiki and more. These treatments are widely available and are even
              taught in India. The Indian medicine market is flourishing with readily available alternatives to
              traditional English medicines.</p>

            <h5 class="mb-0">Language barrier/proficiency</h5>
            <p>Language is not a barrier in India. In addition to being one of the official languages in India,
              English is also widely spoken by the locals. Indian hospitals have multilingual staff, who are
              proficient in English, Hindi, and various other languages. Hospitals adhering to international standards
              have translators available to international patients.</p>

            <h5 class="mb-0">Offerings in Tourism</h5>
            <p>India is a land of many religions, colors, foods, and festivals. The number of tourism options
              available in India is staggering and will satisfy the adventurer, the sightseer, or just the ones that
              are in need of rest and relaxation. Delhi, in the north, has historic monuments such as the Taj Mahal,
              the Red Fort, Akshardham Temple, Qutab Minar, India Gate, the Lotus Temple, Jama Masjid and the
              Parliament House. Jaipur, southwest of Delhi, is known for its opulent structures that were built by
              kings. Some are the City Palace, which includes the Chandra Mahal and Mubarak Mahal, which are a palace
              complex. Moving South, tourists can enjoy houseboat trips on the gorgeous backwaters of Kerala, or enjoy
              the hill stations of Ooty or Kodaikanal in Tamil Nadu. Simply put – from one end to the other, India has
              so much to offer and for those with a variety of interests. For those wanting a taste of modern India,
              the major cities such as Delhi, Mumbai, Bangalore, Pune, and Chennai, have restaurants, movie theaters,
              dance clubs, lounges, shopping malls and more. A single visit to India will leave one longing for their
              next trip there. Fantastic tourism coupled with amazing healthcare makes India an easy choice for
              medical tourism.</p>

            <h5 class="mb-0">Waiting time for the medical help</h5>
            <p>Private hospitals play a crucial and pivotal role in India’s healthcare industry. The Indian healthcare
              system is free from any kind of waitlist or backlog. You can be treated for almost any illness without
              delay. India has a large pool of doctors and they are willing to offer customized treatments, to suit
              the needs of their patients.</p>

            <h5 class="mb-0">Well connected with the world</h5>
            <p>All the major cities in India have airports that are serviced by major international airlines. This
              offers patients a wide range of cities, hospitals and travel options to choose from. India also offers
              other conveniences to patients such as pay-as-you-go phone plans, internet cafes, postal service that
              enables one to stay connected with loved ones in other parts of the world, while in India for treatment.
              Local transport options are also plentiful and easily organized with assistance from your medical
              facilitator.</p>

            <h5 class="mb-0">Government initiatives</h5>
            <p>The Indian healthcare industry is flourishing because it is being endorsed and promoted by the
              government of India. The government has taken several initiatives to ensure the continuous growth of
              this sector in India. Recently relaxed visa laws for abroad patients coming to India also support the
              growth of the sector. There are various forms of assistance in gathering traveling requirements
              including web services and a dedicated helpline for international patients. With the government closely
              monitoring its growth, the industry has all the necessary support required to continue building the
              infrastructure required for growth and sustainability.</p>

